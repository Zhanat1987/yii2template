    Авторизация
</h1>
<hr />
<p>
    Авторизация - это процесс проверки того, что пользователь имеет достаточно прав, чтобы сделать что-то. Yii предоставляет 2 метода авторизации: Access Control Filter (ACF) и Role-Based Access Control (RBAC).
</p>
